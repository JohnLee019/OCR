from gtts import gTTS
from playsound import playsound

# 📖 책 한 페이지 분량의 텍스트 (예시)
book_page = """
요즘은 별일이 없는데도 하루가 어떻게 지나가는지 모르겠다. 아침에 일어나면 항상 똑같은 루틴이 반복되지만, 그 안에서 느끼는 감정은 매일 조금씩 다르다. 어떤 날은 눈 뜨자마자 뭔가 할 수 있을 것 같은 기분이 들기도 하고, 어떤 날은 이불을 걷는 것조차 버거울 때도 있다. 어쩌면 우리가 살아간다는 건 그런 흐름 속에서 균형을 맞추는 일이 아닐까 싶다.

커피를 마시는 시간은 짧지만, 그 안에 담긴 여유는 길게 이어진다. 습관처럼 들린 카페에서 흘러나오는 음악, 창밖을 바라보는 사람들, 종이컵에 남은 온기. 그런 사소한 순간들이 오히려 기억에 오래 남는다. 누군가는 의미 없는 일상이라고 할지 모르지만, 나는 그 안에서 삶의 단서를 찾는다.

가끔은 아무 이유 없이 기분이 좋고, 아무 이유 없이 기분이 나쁘기도 하다. 그런 감정의 기복이 있다는 건 오히려 내가 사람답게 살고 있다는 증거일지도 모른다. 모든 게 매끄럽게만 돌아간다면, 오히려 그게 더 이상한 일이 아닐까?

요즘은 SNS를 보면서 남과 비교하지 않으려고 애쓴다. 남이 잘 사는 걸 보는 건 좋은데, 그게 곧 내 부족함을 확인하는 일이 되는 건 마음이 아프다. 그래서 조금은 천천히, 조금은 엉성하게 살아도 괜찮다고 스스로를 다독인다. 완벽하지 않아도, 그게 나다.
"""

# 🧠 문장 단위로 자르고, 다시 연결 (마침표 기준)
sentences = [s.strip() for s in book_page.split('.') if s.strip()]
combined_text = '. '.join(sentences)  # 문장들 다시 이어 붙이기

# 🎙️ TTS 변환
tts = gTTS(text=combined_text, lang='ko')
tts.save("page.mp3")  # 하나의 mp3로 저장

# ▶️ mp3 재생 (재생이 끝날 때까지 기다림)
playsound("page.mp3")